    public void validar(){
        String correo = txtCorreo.getText();
        String pass = String.valueOf(txtPass.getPassword());
        if (!"".equals(correo) || !"".equals(pass)){
            lg = login.log(correo, pass);//El metodo .log se usa para registrar un mensaje con un parametro de objeto
            if(lg.getCorreo()!= null && lg.getPass()!= null){
                Sistema sis = new Sistema(lg);
                sis.setVisible(true);
                dispose();
            }else{
                JOptionPane.showInternalMessageDialog(null, " Correo o Contraseña incorrecta, vuelva a intentarlo ");
            }
            
        }
    }

    
